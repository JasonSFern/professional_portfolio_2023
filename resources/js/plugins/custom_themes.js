export const getThemeData = (themeName) => {
  if (themeName.includes("--")) {
    let themeNameSplit = themeName.split("--");
    return CustomThemes[themeNameSplit[0]][themeNameSplit[1]];
  } else {
    return CustomThemes[themeName];
  }
}